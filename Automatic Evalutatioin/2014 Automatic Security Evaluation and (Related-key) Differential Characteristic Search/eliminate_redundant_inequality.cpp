int choose(int x[N1][M],int y[N2][M-1],char *filename) 
{ 
	int i, j, temp; 
	int z[N1]={0}; 
	// How many points are not satisfed for each inequality.
	//不满足的点越多，产生的约束越强。 
	for (i=0;i<N1;i++)
	{ 
		for(j=0;j<N2;j++) 
			if((x[i][0]*y[j][0]+x[i][1]*y[j][1]+x[i][2]*y[j][2]+ \
			x[i][3]*y[j][3]+x[i][4]*y[j][4]+x[i][5]*y[j][5] +x[i][6]*y[j][6]+x[i][7]*y[j][7]+x[i][8])<0) 
				z[i]++; 
	}
	
	temp=z[0]; 
	j=0; 
	// Finding the inequality and its count is the largest.
	for(i=1;i<N1;i++)
	{ 
		if(z[i]>temp) 
		{
			j=i;
			temp=z[j];
		} 
	}
	
	if(temp!=0) 
	{ 
		// Delete the points corresponding to the largest inequality.
		for(i=0;i<N2;i++) 
		{ 
			if(x[j][0]*y[i][0]+x[j][1]*y[i][1]+x[j][2]*y[i][2]+x[j][3]*y[i][3]+\
			x[j][4]*y[i][4]+x[j][5]*y[i][5] +x[j][6]*y[i][6]+x[j][7]*y[i][7]+x[j][8]<0)
			{ 
				y[i][0]=0;y[i][1]=0;y[i][2]=0;y[i][3]=0;y[i][4]=0;y[i][5]=0;y[i][6]=0; 
				y[i][7]=0; 
			} 
		}
		
		// Output inequality and the number of points that are not satisfed.
		
		FILE *fp = fopen(filename, "a+");
		for(i=1; i<9;i++) 
		{ 
			if(x[j][i]<0||i==0) 
				fprintf(fp,"%d*x%d",x[j][i],i-1); 
			else 
				fprintf(fp,"+%d*x%d",x[j][i],i-1); 
		}
		
		fprintf(fp,"+%d%6d",x[j][0],temp);fprintf(fp,"\n"); 
		x[j][0]=0;x[j][1]=0;x[j][2]=0;x[j][3]=0;x[j][4]=0;x[j][5]=0;x[j][6]=0;x[j][7]=0; 
		x[j][8]=0;

		return temp; 
	} 
	else 
	return 0; 
}

